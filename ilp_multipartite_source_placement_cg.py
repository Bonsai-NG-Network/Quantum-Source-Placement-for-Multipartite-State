"""
Column-generation variant of the ILP source placement model.

This module does not replace ilp_multipartite_source_placement.py. It adds a
scalable workflow:

    1. Build a small initial candidate-tree set.
    2. Solve the LP-relaxed restricted master problem (RMP).
    3. Use dual variables to price new multipartite Steiner-like trees. The
       default pricing mode is exact over a finite candidate pool generated by
       the same mechanism as the full ILP.
    4. Repeat until no positive reduced-profit tree is found.
    5. Solve the final integer master problem over generated columns.

The final output is still a source placement. The master problems maximize an
expected per-slot throughput surrogate based on the same physical
probabilities as the simulator; entanglement routing, swapping, and fusion are
sampled afterwards by the stochastic simulator.
"""

